import os
from openai import OpenAI

client = OpenAI(api_key=(os.getenv("OPENAI_API_KEY") or "").strip())

TOPIC_RULES = """
주제 고정 규칙:
- 원문 주제를 절대 바꾸지 말 것
- 전쟁/공격/미사일/이란/미군 개입 뉴스면 지정학/전쟁 리스크로만 써라
- 유가 뉴스면 유가/기름값으로만 써라
- 금 뉴스면 금값/안전자산으로만 써라
- 환율 뉴스면 환율/물가/수입비용으로만 써라
- 비트코인 뉴스면 비트코인/코인시장으로만 써라
- 원문에 없는 "정부 발표", "유류세 인상", "전기요금 인상", "부동산" 같은 내용 절대 추가 금지
- 사실을 지어내지 말 것
"""

IMPACT_WORDS = """
강한 표현 우선 규칙:
- 숫자와 함께 쓰기 좋은 동사를 우선 사용
- 제목2는 설명형보다 충격형으로 써라
- 아래 표현을 우선 검토해서 자연스러운 것만 사용:
  터졌다 / 쏠렸다 / 몰렸다 / 몰려든다 / 흔들린다 / 번진다 / 치솟는다 / 커진다 / 번졌다
- "영향이 있다", "가능성이 있다", "전망이다", "우려가 있다" 같은 약한 표현은 최대한 금지
- 짧고 세게 써라
"""

def rewrite(title, desc, mode="normal", number_hint=None):
    if mode == "alert":
        extra = f"""
추가 조건:
- 속보 카드 스타일
- 제목1은 강하게
- 제목2는 지금 당장 체감되는 영향
- 가능하면 "급등", "폭등", "긴장", "충돌", "전쟁", "리스크" 같은 단어 우선
- 제목1은 이미 일어난 것처럼 표현
- 제목2는 반드시 "지금" 또는 "바로" 느낌이 나게 써라
- 숫자가 있으면 최대한 제목1 또는 제목2에 자연스럽게 넣어라
- 사용할 수 있는 숫자 힌트: {number_hint if number_hint else "없음"}
"""
    else:
        extra = f"""
추가 조건:
- 제목1은 이미 일어난 것처럼 표현
- 제목2는 지금 체감 표현
- 숫자가 있으면 최대한 제목1 또는 제목2에 자연스럽게 넣어라
- 사용할 수 있는 숫자 힌트: {number_hint if number_hint else "없음"}
"""

    prompt = f"""
너는 한국 인스타 경제 카드뉴스 카피라이터다.

목표:
원문 주제를 유지한 채, 한국 사람이 바로 이해할 수 있는 짧고 강한 카드 문구를 만든다.

{TOPIC_RULES}

{IMPACT_WORDS}

조건:
- 무조건 한국어
- 매우 짧게
- 제목1: 8~12자
- 제목2: 8~12자
- 설명1/2: 10~18자
- 번역투 금지
- 사람 이름 나열 금지
- 뉴스 제목 그대로 번역 금지
- 설명형 금지
- 추상 표현보다 행동/상태 표현 우선
- 제목1은 상황
- 제목2는 내 돈/시장 반응
- 제목2는 가능하면 숫자 + 행동 구조를 우선
- "영향이 커진다", "시장 불확실성 커진다" 같은 약한 문장보다
  "돈 몰렸다", "지금 흔들린다", "시장 먼저 튄다", "자금 쏠린다" 같은 문장을 우선
{extra}

좋은 예시:
제목1: 유가 +18% 급등
제목2: 지금 기름값 뛴다
설명1: 중동 긴장 커지면
설명2: 에너지부터 반응

제목1: 미군 개입 86%
제목2: 지금 돈 쏠린다
설명1: 전쟁 리스크 커지면
설명2: 투자 심리 먼저 꺾인다

제목1: 환율 1,535원
제목2: 지금 물가 뛴다
설명1: 달러 강세 이어지면
설명2: 수입 부담 더 커진다

제목1: 금값 계속 오른다
제목2: 지금 돈 몰린다
설명1: 불안 심리 커지면
설명2: 안전자산 먼저 간다

제목1: 700억 몰렸다
제목2: 지금 돈 쏠린다
설명1: 긴장감 커질수록
설명2: 자금부터 반응한다

나쁜 예시:
제목1: 원자재 가격 급등
제목2: 제조업 타격 확산

제목1: 유가 증가 전망
제목2: 공급 불안 심화

제목1: 부동산 시장 위축
제목2: 내 돈 흐름 막힌다

제목1: 시장 불확실성 증가
제목2: 투자 심리 약화

영어 뉴스:
제목: {title}
내용: {desc}

출력 형식:
제목1: ...
제목2: ...
설명1: ...
설명2: ...

중요:
- 출력은 반드시 4줄만
- 군더더기 설명 금지
- 숫자가 있으면 최대한 살려라
- 제목2는 최대한 약하지 않게 써라
"""

    res = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[{"role": "user", "content": prompt}],
        temperature=0.4
    )

    text = res.choices[0].message.content.strip().split("\n")

    result = {
        "title1": "시장 다시 흔들린다",
        "title2": "지금 돈 흐름 바뀐다",
        "desc1": "불안 심리 커지면",
        "desc2": "자산 가격 먼저 흔들린다"
    }

    for line in text:
        line = line.strip()
        if line.startswith("제목1:"):
            result["title1"] = line.replace("제목1:", "").strip()
        elif line.startswith("제목2:"):
            result["title2"] = line.replace("제목2:", "").strip()
        elif line.startswith("설명1:"):
            result["desc1"] = line.replace("설명1:", "").strip()
        elif line.startswith("설명2:"):
            result["desc2"] = line.replace("설명2:", "").strip()

    return result